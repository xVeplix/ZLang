package ZLang.data;

public class Console {

    // Dont let initialize the Class
    private Console() {
    }

    // Prints a string
    public static void print(final String z) {
        System.out.println(z);
    }

    // Prints an integer
    public static void printInt(final Integer z) {
        System.out.println(z);
    }

    // Prints an Float
    public static void printFloat(final Float z) {
        System.out.println(z);
    }

    // Prints a Double
    public static void printDouble(final Double z) {
        System.out.println(z);
    }

}
